# Example list
food_stuff_lt = ['apple', 'banana', 'orange', 'carrot', 'potato', 'garlic', 'milk', 'butter', 'butter_milk']

# Slice out the first three items
first_three_items = food_stuff_lt[:3]

# Slice out the last three items
last_three_items = food_stuff_lt[-3:]

# Display the results
print("First Three Items from food_stuff_lt:", first_three_items)
print("Last Three Items from food_stuff_lt:", last_three_items)
#  the [:3] slice extracts the first three items, and [-3:] extracts the last three items from the food_stuff_lt list. 